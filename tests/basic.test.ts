/**
 * Basic test template for expanding later.
 * Use this as a starting point for unit tests.
 */

describe('basic template', () => {
  it('runs a trivial assertion', () => {
    expect(true).toBe(true);
  });

  it('can be expanded with arrange/act/assert', () => {
    // arrange
    const input = 2;

    // act
    const result = input * 2;

    // assert
    expect(result).toBe(4);
  });
});
